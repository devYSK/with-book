package com.apress.springrecipes.reactive.court;

public class SportType {

    private int id;
    private String name;

    public SportType() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
